from geometry import Square, Rectangle

s = Square(2)
print(s.describe)  # Geometry is square with length 2 and breadth 2.
s.area()  # area of square is 4
s.perimeter()  # perimeter of square is 8

r = Rectangle(4, 6)
print(r.describe)  # Geometry is square with length 4 and breadth 6.
r.area()  # area of rectangle is 24
r.perimeter()  # perimeter of rectangle is 20
